import mongoose from "mongoose";

const contactMessageSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  sentAt: {
    type: Date,
    default: Date.now,
  },
});

export const ContactMessage = mongoose.model("ContactMessage", contactMessageSchema);
